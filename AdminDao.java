package com.lyn.dao;

import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;

import com.lyn.model.Admin;

public class AdminDao extends BassDao {
	//管理员登入
	public Admin login(Admin admin) {
	String sql="select * from s_admin where name=? and password=?";//?是占位符
	Admin adminRst=null;
	try {
		PreparedStatement prst=con.prepareStatement(sql);
		//把sql语句传递给数据库对象
		prst.setString(1, admin.getName());
		prst.setString(2, admin.getPassword());
		ResultSet executeQuery = prst.executeQuery();
		//把数据库响应的查询结果存放在ResultSet类对象中供我们使用
		if(executeQuery.next()) {
			adminRst=new Admin();
			adminRst.setId(executeQuery.getInt("id"));
			adminRst.setName(executeQuery.getString("name"));
			adminRst.setPassword(executeQuery.getString("password"));
			adminRst.setCreateDate(executeQuery.getString("createData"));
			
		}
	} catch (SQLException e) {
		// TODO Auto-generated catch block
		e.printStackTrace();
	}
/*	try {
		con.close();
	} catch (SQLException e) {
		// TODO Auto-generated catch block
		e.printStackTrace();
	}*/
	return adminRst;
	}
	public String editPassword(Admin admin,String newPassword) {
		//修改密码函数
		String sql="select * from s_admin where id=? and password=?";//?是占位符
		PreparedStatement prst=null;
		int id=0;
		try {
			//把sql语句传递给数据库对象
			prst=con.prepareStatement(sql);
			prst.setInt(1, admin.getId());
			prst.setString(2, admin.getPassword());
			ResultSet executeQuery = prst.executeQuery();
			//把数据库响应的查询结果存放在ResultSet类对象中供我们使用
			if(!executeQuery.next()) {
				String retString="旧密码错误！";
				return retString;
			}
			id=executeQuery.getInt("id");
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		String retString="修改失败！";
		String sqlString="update s_admin set password = ? where id = ?";
		try {
			prst=con.prepareStatement(sqlString);
			prst.setString(1, newPassword);
			prst.setInt(2, id);
			int rst=prst.executeUpdate();
			if(rst>0) {
				retString="密码修改成功！";
			}	
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		return retString;
	}
}
